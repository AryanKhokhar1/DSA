import java.util.Scanner;

public class Bitwise_left_and_right_shift {
    public static void main(String[] args) {
        // Left shift Bitwise Operator
        System.out.printf("Bitwise Left Shift Operation:\n%d << %d = %d\n",5,1,(5<<1));

        // Right shift Bitwise Operator
        System.out.printf("Bitwise Right Shift Operator:\n%d >> %d = %d\n",5,1,(5>>1));
    }
}
